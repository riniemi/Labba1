#include <iostream>
#include <cmath>

using namespace std;

int main() {
    // Объявляем три переменные для сторон треугольника
    double a, b, c;

    // Просим пользователя ввести стороны
    cout << "Введите первую сторону треугольника: ";
    cin >> a;

    cout << "Введите вторую сторону треугольника: ";
    cin >> b;

    cout << "Введите третью сторону треугольника: ";
    cin >> c;

    cout << endl;

    // ========== ПРОВЕРКА: СУЩЕСТВУЕТ ЛИ ТРЕУГОЛЬНИК? ==========
    // Правило: сумма любых двух сторон должна быть больше третьей
    if (a + b > c && a + c > b && b + c > a) {
        // Если треугольник существует - считаем всё

        // 1. ПЕРИМЕТР
        double perimeter = a + b + c;
        cout << "Периметр треугольника = " << perimeter << endl;

        // 2. ПЛОЩАДЬ ПО ФОРМУЛЕ ГЕРОНА
        double p = perimeter / 2;  // полупериметр
        double area = sqrt(p * (p - a) * (p - b) * (p - c));
        cout << "Площадь треугольника = " << area << endl;

        // 3. ПРОВЕРКА НА РАВНОБЕДРЕННОСТЬ
        if (a == b || a == c || b == c) {
            cout << "Треугольник равнобедренный" << endl;
        } else {
            cout << "Треугольник не равнобедренный" << endl;
        }

    } else {
        // Если треугольник не существует - показываем ошибку
        cout << "ОШИБКА! Такого треугольника не существует!" << endl;
        cout << "Правило: сумма любых двух сторон должна быть больше третьей" << endl;
    }

    return 0;
}
